vcl::mesh marching_cubes::marching_cube(const vcl::grid_3D<float>& values, const int grid_size, const double& isovalue)
{
    vcl::mesh mesh;
    std::map<std::pair<int,int>, unsigned int, int_pair_comparator> edge_to_vertex_index;

    std::array<double,8> current_cell_value;

    vert_positions.clear();
    number_of_triangles_per_vertex.clear();
    edges.clear();

    //loop over every cells
    for(int k_1 = 0; k_1 < grid_size-1; k_1++)
    {
        for(int k_2 = 0; k_2 < grid_size-1; k_2++)
        {
            for(int k_3 = 0; k_3 < grid_size-1; k_3++)
            {
                //position of local cell
                std::array<int,3> X000 {k_1+0, k_2+0, k_3+0};
                std::array<int,3> X001 {k_1+0, k_2+0, k_3+1};
                std::array<int,3> X010 {k_1+0, k_2+1, k_3+0};
                std::array<int,3> X011 {k_1+0, k_2+1, k_3+1};
                std::array<int,3> X100 {k_1+1, k_2+0, k_3+0};
                std::array<int,3> X101 {k_1+1, k_2+0, k_3+1};
                std::array<int,3> X110 {k_1+1, k_2+1, k_3+0};
                std::array<int,3> X111 {k_1+1, k_2+1, k_3+1};


                //value of cell
                current_cell_value[0] = values(X000[0],X000[1],X000[2]) - isovalue;
                current_cell_value[1] = values(X001[0],X001[1],X001[2]) - isovalue;
                current_cell_value[2] = values(X010[0],X010[1],X010[2]) - isovalue;
                current_cell_value[3] = values(X011[0],X011[1],X011[2]) - isovalue;
                current_cell_value[4] = values(X100[0],X100[1],X100[2]) - isovalue;
                current_cell_value[5] = values(X101[0],X101[1],X101[2]) - isovalue;
                current_cell_value[6] = values(X110[0],X110[1],X110[2]) - isovalue;
                current_cell_value[7] = values(X111[0],X111[1],X111[2]) - isovalue;
                




                //select the type of cube
                int type_of_cube = 0;
                if(current_cell_value[0] <= 0) type_of_cube |= 0x01;
                if(current_cell_value[4] <= 0) type_of_cube |= 0x02;
                if(current_cell_value[5] <= 0) type_of_cube |= 0x04;
                if(current_cell_value[1] <= 0) type_of_cube |= 0x08;
                if(current_cell_value[2] <= 0) type_of_cube |= 0x10;
                if(current_cell_value[6] <= 0) type_of_cube |= 0x20;
                if(current_cell_value[7] <= 0) type_of_cube |= 0x40;
                if(current_cell_value[3] <= 0) type_of_cube |= 0x80;

                //look at the LUT to get the corresponding type
                if (EdgeTable[type_of_cube] != 0)
                    create_polygon(current_cell_value, type_of_cube, X000, &mesh, &edge_to_vertex_index, values, grid_size);

            }
        }
    }

    //std::cout << "nb pts " << mesh.position.size() << std::endl;
    //std::cout << "nb normals " << mesh.normal.size() << std::endl;
    //std::cout << "nb edges " << edges.size() << std::endl;
    //std::cout << "nb triangles " << mesh.connectivity.size() << std::endl;
    return mesh;

}

vcl::vec3 vertex_interpolation(vcl::vec3 p1, vcl::vec3 p2, float v1, float v2)
{
    return p1 + (v1/(v1-v2)) * (p2-p1);
    //return (p1+p2)/2.;
}

